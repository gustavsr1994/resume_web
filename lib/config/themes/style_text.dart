import 'package:flutter/widgets.dart';

TextStyle styleTextSmall(Color fontColor, FontWeight fontWeight) {
  return TextStyle(fontSize: 15, color: fontColor, fontWeight: fontWeight);
}

TextStyle styleTextMedium(Color fontColor, FontWeight fontWeight) {
  return TextStyle(fontSize: 25, color: fontColor, fontWeight: fontWeight);
}

TextStyle styleTextLarge(Color fontColor, FontWeight fontWeight) {
  return TextStyle(fontSize: 35, color: fontColor, fontWeight: fontWeight);
}
